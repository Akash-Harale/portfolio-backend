const mongoose = require("mongoose");

const addNewMessageSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    subject: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  },
  { versionKey: false }
);

const AddNewMessage = mongoose.model("contact", addNewMessageSchema);

module.exports = {
  AddNewMessage,
};
